# `&&` and `||` has higher precedence than `and` and `or`
a = true and true || false or true

# To test whether regular expression matches string, use `=~` operator
/old/ =~ 'old house'

# there is a comparision operator `<=>`, and will return integer with -1, 0 or 1.
a = 2
b = 3
a <=> b # will return 1


